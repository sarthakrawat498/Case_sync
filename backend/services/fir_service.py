"""
FIR Draft Generation Service
Generates First Information Report drafts using strict templates.
NO LLM/AI hallucination - purely template-based generation.

Supports:
- English FIR template
- Hindi FIR template (Devanagari)
"""

from typing import Dict, Any, Optional
from datetime import datetime
import uuid

from core.config import settings


class FIRService:
    """
    FIR Draft generation service using strict templates.
    Generates deterministic, structured FIR documents.
    """
    
    def __init__(self):
        """Initialize FIR service with templates."""
        self.english_template = self._get_english_template()
        self.hindi_template = self._get_hindi_template()
    
